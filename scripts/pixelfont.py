_FALLBACK = FONT["?"]


def width(text, scale=1):
    """Rendered width in virtual pixels, excluding the trailing inter-glyph gap."""
    if not text:
        return 0
    return (len(str(text)) * ADVANCE - 1) * scale


def height(scale=1):
    return GLYPH_H * scale


def blit(canvas, text, x, y, color, scale=1):
    """Draw text with its top-left corner at (x, y). Returns the x it ended at."""
    cx = x
    for ch in str(text).upper():
        rows = FONT.get(ch, _FALLBACK)
        for ry, row in enumerate(rows):
            run = 0
            for rx in range(GLYPH_W + 1):
                lit = rx < GLYPH_W and row[rx] == "#"
                if lit:
                    run += 1
                elif run:
                    # Emit each horizontal run as a single rect rather than one
                    # rect per pixel. Roughly a 3x reduction in output size.
                    canvas.px(cx + (rx - run) * scale, y + ry * scale,
                              run * scale, scale, color)
                    run = 0
        cx += ADVANCE * scale
    return cx - scale


def fit(text, max_px, scale=1):
    """Truncate text to max_px, marking the cut with '..' if anything was lost.

    Layout here is computed, never eyeballed: a label and a right-aligned value
    that overlap by three pixels look like a typo, and at this glyph size there
    is no slack to absorb a bad guess.
    """
    text = str(text)
    if width(text, scale) <= max_px:
        return text
    ell = width("..", scale)
    keep = len(text)
    while keep > 0 and width(text[:keep], scale) + ell > max_px:
        keep -= 1
    return text[:keep].rstrip(" ,.-") + ".." if keep > 0 else ""


def blit_centered(canvas, text, cx, y, color, scale=1):
    return blit(canvas, text, cx - width(text, scale) // 2, y, color, scale)


def blit_right(canvas, text, right_x, y, color, scale=1):
    return blit(canvas, text, right_x - width(text, scale), y, color, scale)
